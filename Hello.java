// Every Java Program must define a class and all code is inside a class
// Everything in Java must hae a type
// Every Java program must have a function called public static void main(String[] args)
public class Hello{
    // public = this method can be called by anyone
    // static = this is a method of the class, but not any specific instance of the class
    // void = the method 'main' will not return a value
    
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}
